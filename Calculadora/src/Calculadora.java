public class Calculadora {
  
  public Integer somar(int num1, int num2) {
    return num1 + num2;
  }

  public Integer subtrair(int num1, int num2) {
    return num1 - num2;
  }

  public Integer multiplicar(int num1, int num2) {
    return num1 * num2;
  }

  public Integer dividir(int num1, int num2) {
    return num1 / num2;
  }
}
